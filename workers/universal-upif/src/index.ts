// ── Types ────────────────────────────────────────────────────────────────────

type EntityType = 'HUMAN' | 'AI_AGENT' | 'IOT_DEVICE' | 'SYNTHETIC' | 'ORGANISATION' | 'ECOSYSTEM';
type IdentityTier = 'BASIC' | 'VERIFIED' | 'TRUSTED' | 'SOVEREIGN' | 'UNIVERSAL';
type PillarAccess = 'PIL1' | 'PIL2' | 'PIL3' | 'PIL4' | 'PIL5' | 'PIL6';

interface UPIFIdentity {
  upifId: string;              // UPIF-{hash}-{version}
  dpid: string;
  entityType: EntityType;
  tier: IdentityTier;
  displayName: string;
  createdAt: string;
  lastActive: string;
  locations: string[];         // L01-L23 accessible
  pillars: PillarAccess[];     // PIL1-PIL6 accessible
  capabilities: string[];
  reputation: ReputationScore;
  gdpContribution: AgenticGDP;
  biometricAnchored: boolean;
  l402Capable: boolean;
  passportId?: string;         // Slipstream passport if issued
  metadata: Record<string, unknown>;
}

interface ReputationScore {
  overall: number;             // 0-1000
  dimensions: {
    reliability: number;       // 0-100
    contribution: number;      // 0-100
    security: number;          // 0-100
    collaboration: number;     // 0-100
    innovation: number;        // 0-100
  };
  lastUpdated: string;
  evidenceCount: number;
  trend: 'ASCENDING' | 'STABLE' | 'DESCENDING';
}

interface AgenticGDP {
  // Agentic GDP = economic value generated by this participant
  totalContribution: number;   // in TRAN units
  breakdown: {
    dataGenerated: number;
    servicesProvided: number;
    transactionsProcessed: number;
    knowledgeContributed: number;
    securityContributed: number;
  };
  rank: number;                // universe-wide rank
  percentile: number;          // top X% of all participants
  period: string;              // 'lifetime' | 'monthly' | 'weekly'
  lastCalculated: string;
}

interface IdentityFederation {
  federationId: string;
  primaryUpifId: string;
  federatedIdentities: Array<{
    system: string;
    externalId: string;
    verifiedAt: string;
    trustLevel: 'LOW' | 'MEDIUM' | 'HIGH';
  }>;
  crossLocationProof: string;  // cryptographic proof of cross-location identity
  validUntil: string;
}

interface UPIFStats {
  totalIdentities: number;
  byEntityType: Record<EntityType, number>;
  byTier: Record<IdentityTier, number>;
  totalAgenticGDP: number;
  activeToday: number;
  newThisWeek: number;
  biometricAnchored: number;
  l402Capable: number;
}

// ── Identity Registry (in-memory simulation) ──────────────────────────────────

const identityRegistry = new Map<string, UPIFIdentity>();

function generateUpifId(): string {
  const hash = Math.random().toString(36).slice(2, 10).toUpperCase();
  return `UPIF-${hash}-V3`;
}

function generateReputation(tier: IdentityTier): ReputationScore {
  const tierBase: Record<IdentityTier, number> = {
    BASIC: 200, VERIFIED: 400, TRUSTED: 600, SOVEREIGN: 800, UNIVERSAL: 950,
  };
  const base = tierBase[tier];
  return {
    overall: base + Math.floor(Math.random() * (1000 - base) * 0.3),
    dimensions: {
      reliability: Math.min(100, Math.floor(base / 10 + Math.random() * 20)),
      contribution: Math.min(100, Math.floor(base / 10 + Math.random() * 25)),
      security: Math.min(100, Math.floor(base / 10 + Math.random() * 15)),
      collaboration: Math.min(100, Math.floor(base / 10 + Math.random() * 20)),
      innovation: Math.min(100, Math.floor(base / 10 + Math.random() * 30)),
    },
    lastUpdated: new Date().toISOString(),
    evidenceCount: Math.floor(Math.random() * 500) + 10,
    trend: Math.random() > 0.3 ? 'ASCENDING' : Math.random() > 0.5 ? 'STABLE' : 'DESCENDING',
  };
}

function generateAgenticGDP(): AgenticGDP {
  const data = Math.floor(Math.random() * 10000);
  const services = Math.floor(Math.random() * 50000);
  const transactions = Math.floor(Math.random() * 100000);
  const knowledge = Math.floor(Math.random() * 5000);
  const security = Math.floor(Math.random() * 20000);
  const total = data + services + transactions + knowledge + security;

  return {
    totalContribution: total,
    breakdown: { dataGenerated: data, servicesProvided: services, transactionsProcessed: transactions, knowledgeContributed: knowledge, securityContributed: security },
    rank: Math.floor(Math.random() * 100000) + 1,
    percentile: Math.round(Math.random() * 100 * 10) / 10,
    period: 'lifetime',
    lastCalculated: new Date().toISOString(),
  };
}

function createIdentity(
  entityType: EntityType,
  tier: IdentityTier,
  displayName: string,
  metadata: Record<string, unknown> = {}
): UPIFIdentity {
  const upifId = generateUpifId();
  const allLocations = Array.from({ length: 23 }, (_, i) => `L${String(i + 1).padStart(2, '0')}`);
  const tierLocationCount: Record<IdentityTier, number> = {
    BASIC: 3, VERIFIED: 8, TRUSTED: 15, SOVEREIGN: 20, UNIVERSAL: 23,
  };
  const locCount = tierLocationCount[tier];
  const locations = allLocations.slice(0, locCount);

  const tierPillarCount: Record<IdentityTier, number> = {
    BASIC: 1, VERIFIED: 2, TRUSTED: 4, SOVEREIGN: 5, UNIVERSAL: 6,
  };
  const pillars = (['PIL1','PIL2','PIL3','PIL4','PIL5','PIL6'] as PillarAccess[]).slice(0, tierPillarCount[tier]);

  const identity: UPIFIdentity = {
    upifId,
    dpid: 'DPID-ORC-UPF-001',
    entityType,
    tier,
    displayName,
    createdAt: new Date().toISOString(),
    lastActive: new Date().toISOString(),
    locations,
    pillars,
    capabilities: entityType === 'AI_AGENT'
      ? ['reasoning', 'planning', 'tool-use', 'code-execution']
      : entityType === 'IOT_DEVICE'
      ? ['sensor-data', 'actuation', 'edge-compute']
      : ['data-access', 'service-consumption', 'knowledge-creation'],
    reputation: generateReputation(tier),
    gdpContribution: generateAgenticGDP(),
    biometricAnchored: tier === 'TRUSTED' || tier === 'SOVEREIGN' || tier === 'UNIVERSAL',
    l402Capable: tier !== 'BASIC',
    metadata,
  };

  identityRegistry.set(upifId, identity);
  return identity;
}

