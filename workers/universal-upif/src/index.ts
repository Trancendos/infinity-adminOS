function getStats(): UPIFStats {
  const identities = Array.from(identityRegistry.values());
  const byType = identities.reduce((acc, id) => {
    acc[id.entityType] = (acc[id.entityType] ?? 0) + 1;
    return acc;
  }, {} as Record<EntityType, number>);
  const byTier = identities.reduce((acc, id) => {
    acc[id.tier] = (acc[id.tier] ?? 0) + 1;
    return acc;
  }, {} as Record<IdentityTier, number>);

  return {
    totalIdentities: identityRegistry.size,
    byEntityType: byType,
    byTier: byTier,
    totalAgenticGDP: identities.reduce((sum, id) => sum + id.gdpContribution.totalContribution, 0),
    activeToday: Math.floor(identityRegistry.size * 0.7),
    newThisWeek: 47,
    biometricAnchored: identities.filter(id => id.biometricAnchored).length,
    l402Capable: identities.filter(id => id.l402Capable).length,
  };
}

// ── Identity CRUD ─────────────────────────────────────────────────────────────

const IssueSchema = z.object({
  entityType: z.enum(['HUMAN', 'AI_AGENT', 'IOT_DEVICE', 'SYNTHETIC', 'ORGANISATION', 'ECOSYSTEM']),
  tier: z.enum(['BASIC', 'VERIFIED', 'TRUSTED', 'SOVEREIGN', 'UNIVERSAL']).default('BASIC'),
  displayName: z.string().min(2).max(100),
  metadata: z.record(z.unknown()).optional(),
});

app.post('/identity/issue', async (c) => {
  const body = await c.req.json();
  const parsed = IssueSchema.safeParse(body);
  if (!parsed.success) return c.json({ error: 'Invalid request', details: parsed.error.issues }, 400);

  const identity = createIdentity(
    parsed.data.entityType,
    parsed.data.tier,
    parsed.data.displayName,
    parsed.data.metadata ?? {}
  );

  return c.json({ issued: true, identity }, 201);
});

app.get('/identity/:upifId', (c) => {
  const upifId = c.req.param('upifId');
  const identity = identityRegistry.get(upifId);
  if (!identity) return c.json({ error: 'Identity not found', upifId }, 404);
  return c.json(identity);
});

app.get('/identity/:upifId/gdp', (c) => {
  const upifId = c.req.param('upifId');
  const identity = identityRegistry.get(upifId);
  if (!identity) return c.json({ error: 'Identity not found' }, 404);
  return c.json({
    upifId,
    gdpContribution: identity.gdpContribution,
    reputation: identity.reputation,
    agenticGDPShare: Math.round((identity.gdpContribution.totalContribution / 50000000) * 10000) / 100,
    generatedAt: new Date().toISOString(),
  });
});

// ── Resolution ────────────────────────────────────────────────────────────────

app.get('/resolve/:locationId', (c) => {
  const locationId = c.req.param('locationId');
  if (!locationId.match(/^L(0[1-9]|1[0-9]|2[0-3])$/)) {
    return c.json({ error: 'Invalid location ID. Use L01-L23' }, 400);
  }

  const identities = Array.from(identityRegistry.values())
    .filter(id => id.locations.includes(locationId));

  return c.json({
    locationId,
    dpid: 'DPID-ORC-UPF-001',
    resolvedAt: new Date().toISOString(),
    totalIdentities: identities.length,
    identities: identities.slice(0, 20),
  });
});

