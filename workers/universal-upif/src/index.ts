app.post('/identity/:upifId/federate', async (c) => {
  const upifId = c.req.param('upifId');
  const identity = identityRegistry.get(upifId);
  if (!identity) return c.json({ error: 'Identity not found' }, 404);

  const body = await c.req.json();
  const federation: IdentityFederation = {
    federationId: `FED-${Date.now()}`,
    primaryUpifId: upifId,
    federatedIdentities: body.externalIdentities ?? [],
    crossLocationProof: `PROOF-${Math.random().toString(36).slice(2, 18).toUpperCase()}`,
    validUntil: new Date(Date.now() + 90 * 86400000).toISOString(),
  };

  return c.json({ federated: true, federation }, 201);
});

