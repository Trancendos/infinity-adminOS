app.get('/search', (c) => {
  const q = c.req.query('q') ?? '';
  const type = c.req.query('type');
  const tier = c.req.query('tier');

  let results = Array.from(identityRegistry.values());
  if (q) results = results.filter(id => id.displayName.toLowerCase().includes(q.toLowerCase()) || id.upifId.includes(q));
  if (type) results = results.filter(id => id.entityType === type.toUpperCase());
  if (tier) results = results.filter(id => id.tier === tier.toUpperCase());

  return c.json({
    query: q,
    totalResults: results.length,
    results: results.slice(0, 20),
  });
});

